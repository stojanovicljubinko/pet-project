    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        FileWriter myWriter;
           try {
                myWriter = new FileWriter("filename.txt");
        if (this.jComboBox1.getSelectedItem() == "Macka") {
            String pedigre = "nema";
            if (jCheckBox1.isSelected()) {
                pedigre = "ima";
            }
            Macka macka = new Macka(jTextField1.getText(), jTextField2.getText(), (String) jComboBox1.getSelectedItem(), pedigre);
             myWriter.write(macka.returnString());
        } else if (this.jComboBox1.getSelectedItem() == "Pas") {
            String pedigre = "nema";
            if (jCheckBox1.isSelected()) {
                pedigre = "ima";
            }
            Pas pas = new Pas(jTextField1.getText(), jTextField2.getText(), (String) jComboBox1.getSelectedItem(), pedigre);
            myWriter.write(pas.returnString());
        }
            myWriter.close();
        } catch (IOException ex) {
            Logger.getLogger(Dodavanje.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String text = null;
        try { 
            FileReader fr = new FileReader("filename.txt");
             char [] a = new char[50];
             fr.read(a); 
             for (char c : a) {
                text += c;
            }
             jTextArea1.append(text);
             fr.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Dodavanje.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Dodavanje.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

